-- Phase 9: comments (insert-only in the UI), owned by the commenter via
-- the direct users FK — comments hang off users in the graph, so the
-- owner walk is created_by, not the article's owner.
CREATE TABLE IF NOT EXISTS comments (
    id         integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    edition_id integer NOT NULL REFERENCES article_editions(id),
    created_by integer REFERENCES users(id),
    body       text NOT NULL DEFAULT '',
    created_at timestamptz NOT NULL DEFAULT now()
);

-- Pro/con marks: insert-once by the UNIQUE (comment_id, user_id)
-- constraint, per RESOLVED (the KF v1.0 shape) — a second mark by the
-- same user is a database rejection answered 400, and changing a mark is
-- undesigned upsert territory, deliberately out. The surrogate id exists
-- because rows are id-addressable by framework contract (insert RETURNING
-- id, broadcasts carry id).
CREATE TABLE IF NOT EXISTS comment_marks (
    id         integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    comment_id integer NOT NULL REFERENCES comments(id),
    user_id    integer NOT NULL REFERENCES users(id),
    kind       text NOT NULL CHECK (kind IN ('pro', 'con')),
    UNIQUE (comment_id, user_id)
);

-- Expositions: a rule-driven shelf of published articles. The rules are
-- ordinary rows an ordinary collection edits; the has-tag/lacks-tag SQL
-- lives in queries/expositionArticles.sql.
CREATE TABLE IF NOT EXISTS expositions (
    id          integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    created_by  integer REFERENCES users(id),
    title       text NOT NULL DEFAULT '',
    description text NOT NULL DEFAULT ''
);

CREATE TABLE IF NOT EXISTS exposition_tag_rules (
    id            integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    exposition_id integer NOT NULL REFERENCES expositions(id) ON DELETE CASCADE,
    tag_id        integer NOT NULL REFERENCES tags(id),
    rule          text NOT NULL CHECK (rule IN ('has', 'lacks'))
);

-- Deleting an exposition takes its rules with it — the rules are the
-- exposition's definition, meaningless without it. Drop/re-add keeps the
-- upgrade idempotent for databases created before the cascade.
ALTER TABLE exposition_tag_rules DROP CONSTRAINT IF EXISTS exposition_tag_rules_exposition_id_fkey;
ALTER TABLE exposition_tag_rules ADD CONSTRAINT exposition_tag_rules_exposition_id_fkey
    FOREIGN KEY (exposition_id) REFERENCES expositions(id) ON DELETE CASCADE;
