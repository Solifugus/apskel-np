-- Knowledge Foyer v1.0 (Phase 9). Identity tables come from
-- server/identity.sql, applied first by run.js, so created_by can
-- reference users. Everything here is idempotent — run.js reapplies the
-- schema on every start.

CREATE TABLE IF NOT EXISTS articles (
    id         integer PRIMARY KEY,
    created_by integer REFERENCES users(id),
    created_at timestamptz NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS article_editions (
    id         integer PRIMARY KEY,
    article_id integer NOT NULL REFERENCES articles(id),
    title      text NOT NULL DEFAULT '',
    body       text NOT NULL DEFAULT '',
    revision   integer NOT NULL DEFAULT 0
);

-- Phase 9: the publish workflow. status is a plain column — publish is a
-- status write through the ordinary Wire, per RESOLVED (publish is a
-- status write).
ALTER TABLE article_editions ADD COLUMN IF NOT EXISTS status text NOT NULL DEFAULT 'draft';
ALTER TABLE article_editions ADD COLUMN IF NOT EXISTS published_at timestamptz;

-- Next editions come from the composer; give them server-assigned ids
-- without disturbing the seeded 1/2.
CREATE SEQUENCE IF NOT EXISTS article_editions_id_seq OWNED BY article_editions.id;
-- Never move the sequence BACKWARDS: this reapplies on every start, and
-- if rows at the top were deleted, a plain MAX(id) would rewind the
-- sequence and re-mint deleted ids (eventually colliding with a live
-- row). last_value is the floor.
SELECT setval('article_editions_id_seq', GREATEST(
    100,
    (SELECT last_value FROM article_editions_id_seq),
    (SELECT COALESCE(MAX(id), 0) FROM article_editions)));
ALTER TABLE article_editions ALTER COLUMN id SET DEFAULT nextval('article_editions_id_seq');

-- Published editions are immutable at the schema, per the RESOLVED entry
-- of that name: integrity is the app's SQL, exactly like its FKs. The
-- one-way door: publishing is an UPDATE on a still-draft row (which this
-- trigger also timestamps); once status is 'published', UPDATE and DELETE
-- are rejected — owner included. The framework answers the rejection as a
-- 400 carrying this message.
CREATE OR REPLACE FUNCTION kf_published_immutable() RETURNS trigger AS $$
BEGIN
    IF OLD.status = 'published' THEN
        RAISE EXCEPTION 'published editions are immutable — corrections go in the next edition';
    END IF;
    IF TG_OP = 'UPDATE' AND NEW.status = 'published' AND OLD.status = 'draft' THEN
        NEW.published_at := now();
    END IF;
    IF TG_OP = 'DELETE' THEN
        RETURN OLD;
    END IF;
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

DROP TRIGGER IF EXISTS kf_published_immutable ON article_editions;
CREATE TRIGGER kf_published_immutable
    BEFORE UPDATE OR DELETE ON article_editions
    FOR EACH ROW EXECUTE FUNCTION kf_published_immutable();

-- One draft at a time per article: the draft IS the next edition in
-- progress; a second "Start next edition" press is a database rejection
-- answered 400, same doctrine as the immutability trigger. (A database
-- holding duplicate drafts fails here at startup — resolve by hand;
-- silently deleting a draft is not the schema's call.)
CREATE UNIQUE INDEX IF NOT EXISTS kf_one_draft_per_article
    ON article_editions (article_id) WHERE status = 'draft';

